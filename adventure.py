def start_game():
    print("\n--- مرحلة الكهف ---")
    ans1 = input("سؤال 1: ما هو الشيء الذي له أسنان ولا يعض؟ (comb): ")
    
    if ans1 == "comb":
        print("أحسنت! لننتقل للسؤال الثاني.")
        ans2 = input("سؤال 2: ما هو لون السماء في يوم صافٍ؟ (blue): ")
        if ans2 == "blue":
            print("مبارك! لقد فزت بكل المراحل!")
        else:
            print("للأسف، إجابة خاطئة في السؤال الثاني.")
    else:
        print("إجابة خاطئة! انتهت اللعبة.")

start_game()
